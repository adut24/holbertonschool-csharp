namespace Enemies
{
	/// <summary>
	/// Represents a zombie enemy
	/// </summary>
	class Zombie
	{
		/// <summary>
		/// Represents the zombie's life.
		/// </summary>
		public int health;

		/// <summary>
		/// Initializes a new instance of the <see cref="Zombie"/>class.
		/// </summary>
		public Zombie() => health = 0;
	}
}
